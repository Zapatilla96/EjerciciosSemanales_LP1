    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int fila = tblPEDIDOS.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un registro!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String id = tblPEDIDOS.getValueAt(tblPEDIDOS.getSelectedRow(), 0).toString();
        try {
            cadenaSQL = "SELECT * FROM pedidos WHERE id = " + id;
            resultado = controlador.consultar(cadenaSQL);
            if (resultado.next()) {
                txtID.setText(resultado.getString(1));
                recuperarValorCombo(cboCLIENTES, resultado.getString(2));
                recuperarValorCombo(cboPRODUCTOS, resultado.getString(3));
                txtFecha.setDate(formatoFECHA.parse(resultado.getString(4)));
                txtPRECIO.setText(resultado.getString(5));
                txtCANTIDAD.setText(resultado.getString(6));
                txtTOTAL.setText(resultado.getString(7));
                tabExistencias.setSelectedIndex(0);
                txtPRECIO.grabFocus();
                editar = true;
            }
        } catch (SQLException ex) {
            System.err.println("ERROR SQL EDICION: " + ex);
        } catch (ParseException ex) {
            System.err.println("ERROR PARSE DATOS: " + ex);
        }
        controlador.desconectar();
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblPEDIDOS.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un registro!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int pregunta = JOptionPane.showConfirmDialog(this, "Borrar Registro?", "ATENCION", JOptionPane.YES_NO_OPTION);
        if (pregunta == JOptionPane.NO_OPTION) {
            return;
        }
        String id = tblPEDIDOS.getValueAt(tblPEDIDOS.getSelectedRow(), 0).toString();
        String cadenaSQL = "DELETE FROM pedidos WHERE id = " + id;
        boolean operacion = controlador.actualizar(cadenaSQL);
        System.err.println("Operacion: " + operacion);
        inicializar();
        tabExistencias.setSelectedIndex(1);

    }//GEN-LAST:event_btnEliminarActionPerformed

