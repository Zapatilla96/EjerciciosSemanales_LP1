    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String id = txtID.getText();
        String fecha = funciones.convertirFecha(txtFecha);
        String precio = txtPRECIO.getText();
        String cantidad = txtCANTIDAD.getText();
        String total = txtTOTAL.getText();
        String cliente = ((ComboBox) cboCLIENTES.getSelectedItem()).getId();
        String producto = ((ComboBoxT) cboPRODUCTOS.getSelectedItem()).getId();

        if (precio.isEmpty() || cantidad.isEmpty() || fecha.isEmpty()) {
            String mensaje = "Todos los datos son obligatorios!";
            JOptionPane.showMessageDialog(this, mensaje);
            return;
        }

        if (!editar) {
            cadenaSQL = "INSERT INTO pedidos VALUES (";
            cadenaSQL += id;
            cadenaSQL += ",";
            cadenaSQL += cliente;
            cadenaSQL += ",";
            cadenaSQL += producto;
            cadenaSQL += ",'";
            cadenaSQL += fecha;
            cadenaSQL += "',";
            cadenaSQL += precio;
            cadenaSQL += ",";
            cadenaSQL += cantidad;
            cadenaSQL += ",";
            cadenaSQL += total;
            cadenaSQL += ")";
        } else {
            cadenaSQL = "UPDATE pedidos SET ";
            cadenaSQL += "id_clientes=" + cliente;
            cadenaSQL += ",";
            cadenaSQL += "id_productos=" + producto;
            cadenaSQL += ",'";
            cadenaSQL += "fecha=" + fecha;
            cadenaSQL += "',";
            cadenaSQL += "precioUnitario=" + precio;
            cadenaSQL += ",";
            cadenaSQL += "cantidad=" + cantidad;
            cadenaSQL += ",";
            cadenaSQL += "total=" + total;
            cadenaSQL += " WHERE id=" + id;
        }
        boolean operacion = controlador.actualizar(cadenaSQL);
        System.err.println("Operacion: " + operacion);
        inicializar();
        tabExistencias.setSelectedIndex(1);
    }//GEN-LAST:event_btnGuardarActionPerformed

